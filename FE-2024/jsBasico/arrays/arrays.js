/*
Estrutura de dados que armazena uma coleção de valores
*/
// Declarando um array
let linguagens = ["PHP", "Python", "Java"];

// Acessando um elemento do array
console.log(linguagens[0]); // Saída: PHP

// Adicionando um elemento ao array
linguagens.push("C");
console.log(linguagens); // Saída: ["PHP", "Python", "Java", "C"]
